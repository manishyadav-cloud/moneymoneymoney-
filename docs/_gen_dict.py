output = []
for row in stats:
    col = row['column_name'].strip()
    col_norm = norm(col)
    defn = DEFS.get(col_norm, None)
    if defn is None:
        if col_norm.startswith('udf'):
            defn = 'User-Defined Field (custom merchant data)'
        elif col_norm.startswith('field') or col_norm.startswith('field_'):
            defn = 'Reserved/custom field — confirm with finance team'
        elif col_norm.startswith('column'):
            defn = 'Trailing column — likely unused'
        elif col_norm.startswith('shipping_'):
            defn = f'Shipping {col_norm.replace("shipping_", "").replace("_", " ")}'
        else:
            defn = '[NEEDS DEFINITION]'
    output.append({
        'Table': row['table_name'],
        'Column': col,
        'Data Type': row['data_type'],
        'Definition': defn,
        'Null %': row['null_pct'],
        'Distinct Count': row['distinct_count'],
        'Sample Values': row['sample_values'][:200],
        'Finance Team Notes': ''
    })

with open('docs/DATA_DICTIONARY_DRAFT.csv', 'w', newline='', encoding='utf-8') as f:
    writer = csv.DictWriter(f, fieldnames=output[0].keys())
    writer.writeheader()
    writer.writerows(output)

defined = sum(1 for o in output if o['Definition'] != '[NEEDS DEFINITION]')
undefined = sum(1 for o in output if o['Definition'] == '[NEEDS DEFINITION]')
print(f"Total columns: {len(output)}")
print(f"Defined: {defined}")
print(f"Needs definition: {undefined}")
if undefined > 0:
    print("\n--- Columns needing definition ---")
    for o in output:
        if o['Definition'] == '[NEEDS DEFINITION]':
            print(f"  {o['Table']:45s} {o['Column']}")
